package com.niit.backend.model;

public class LoginModel {

	private String registerEmail;
	private String registerName;
	private String registerRole;
	private String registerGender;
	private String registerPassword;

	public String getRegisterEmail() {
		return registerEmail;
	}

	public void setRegisterEmail(String registerEmail) {
		this.registerEmail = registerEmail;
	}

	public String getRegisterName() {
		return registerName;
	}

	public void setRegisterName(String registerName) {
		this.registerName = registerName;
	}

	public String getRegisterRole() {
		return registerRole;
	}

	public void setRegisterRole(String registerRole) {
		this.registerRole = registerRole;
	}

	public String getRegisterGender() {
		return registerGender;
	}

	public void setRegisterGender(String registerGender) {
		this.registerGender = registerGender;
	}

	public String getRegisterPassword() {
		return registerPassword;
	}

	public void setRegisterPassword(String registerPassword) {
		this.registerPassword = registerPassword;
	}

}
